export type StandardScore = {
  key: StandardKey
  code: string
  name: string
  yesCount: number
  total: number
  priority: Priority
  recommendation: string
}

export function scoreStandard(key: StandardKey, yesCount: number): StandardScore {
  const std = DIAGNOSTIC_STANDARDS.find((s) => s.key === key)!
  let priority: Priority
  let recommendation: string
  if (yesCount >= 4) {
    priority = 'high'
    recommendation = 'Рекомендуется к внедрению или сертификации.'
  } else if (yesCount >= 2) {
    priority = 'medium'
    recommendation = 'Требуется дополнительная диагностика.'
  } else {
    priority = 'low'
    recommendation = 'Обычно не является приоритетом.'
  }
  return {
    key,
    code: std.code,
    name: std.name,
    yesCount,
    total: std.questions.length,
    priority,
    recommendation,
  }
}

export function overallRecommendation(scores: StandardScore[]): string {
  const high = scores.filter((s) => s.priority === 'high')
  if (high.length === 0) {
    return 'По результатам диагностики ни одна система не набрала высокий приоритет. Рекомендуется дополнительная диагностика по системам со средним приоритетом.'
  }
  if (high.length === 1) {
    return `Рекомендуется внедрение ${high[0].code} (${high[0].name}).`
  }
  const list = high.map((s) => s.code).join(', ')
  return `Рекомендуется интегрированная система менеджмента (ИСМ), объединяющая: ${list}. Единая политика, цели, риски, процедуры, внутренний аудит и анализ руководства.`
}
